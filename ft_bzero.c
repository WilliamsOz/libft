#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*ptrs;
	size_t	i;

	i = 0;
	ptrs = (char*)s;
	while (i < n)
	{
		ptrs[i] = '\0';
		i++;
	}
}
